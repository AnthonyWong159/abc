package calculator;

public class calculate {
	static void add(float x, float y) {		
		System.out.println(x + " + " + y + " = " + (x+y));		
	}
	static void minus(float x, float y) {
		System.out.println(x + " - " + y + " = " + (x-y));
	}
	static void multiply(float x, float y) {
		System.out.println(x + " * " + y + " = " + (x*y));
	}
	static void divide(float x, float y) {
		System.out.println(x + " / " + y + " = " + (x/y));
	}
}
